//Render
void Level::Render(gef::Renderer3D* renderer_3d_,gef::Platform& platform)
{
	//Set Material texture
	gef::Material mat;
	mat.set_texture(CreateTextureFromPNG("textures/block_texture.png", platform));
	renderer_3d_->set_override_material(&mat);
	//Draw Objects
	for (int i = 0; i < platforms.size(); i++)
	{
		renderer_3d_->DrawMesh(*platforms[i]);
	}

	renderer_3d_->set_override_material(NULL);
}
//Level Builder
void Level::LevelBuilder(PrimitiveBuilder* primitive_builder_, b2World* world,std::vector<int> map)
{
	//Iterate through Map Vector
	for (int i = 0; i < map.size(); i++)
	{
		//If is 1 Then Create Block
		if (map[i] == 1)
		{
			//Set POSITION
			gef::Vector2 pos = { 0,0 };
			pos.x = (i % 43)- 21;
			pos.y = -(( i- (i % 43) )/43 - 13);

			//Create Game Object
			GameObject* gameObj = new GameObject;
			//Init
			gameObj->Init(primitive_builder_, world, pos, { 0.5,0.5,1 });
			gameObj->body->SetAwake(false);
			gameObj->body->SetSleepingAllowed(true);
			platforms.push_back(gameObj);
		}
	}
}

//Clean Up
void Level::CleanUp()
{
	for (int i = 0; i < platforms.size(); i++)
	{
		delete platforms[i];
		platforms[i] = NULL;
	}
}
